	<h1>Rules</h1>

	<p>
		Each player play after one another.
		To play, the player moves his pawn to the country displayed on the card, if he guess the country correctly he get one point.
	</p>
		Roll a dice :
		<ul>
			<li>1: give the population of the country</li>
			<li>2: give the capital of the country</li>
			<li>3: give the languages of the country</li>
			<li>4: give the currency of the country</li>
			<li>5: give the name of a random landmark amoung the 4 displayed on the card</li>
			<li>6: ask another player to read the definition of a dish or drink and give its name</li>
		</ul>
		If the player win he the card is withdrawn and the player get a point<br/>
		If the country of the next card can be reach by land for the player current position 
		it plays again if not it is the next player turn.
	<p>
		The player that first reach 20 points win the game.
	</p>
		

